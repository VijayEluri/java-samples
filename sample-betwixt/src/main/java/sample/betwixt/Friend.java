package sample.betwixt;


public class Friend {
    String name;
    String type;
    int age;

    public int getAge() {
        return age;
    }

    public String getType() {
        return type;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String friend) {
        this.name = friend;
    }
}
